package HomePractice;

public class Calc {

	public float add(float a, float b) {
		float c;
		c = a + b;
		return c;
	}

	public float sub(float a, float b) {
		float c;
		c = a - b;
		return c;
	}

	public float mul(float a, float b) {
		float c;
		c = a * b;
		return c;
	}

	public float div(float a, float b) {
		float c;
		c = a / b;
		return c;
	}

	public float mod(float a, float b) {
		float c;
		c = a % b;
		return c;
	}

}
